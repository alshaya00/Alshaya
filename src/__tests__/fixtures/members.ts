export function getMemberById(id: string): FamilyMemberFixture | undefined {
  return familyMemberFixtures.find((m) => m.id === id);
}

export function getMembersByGeneration(generation: number): FamilyMemberFixture[] {
  return familyMemberFixtures.filter((m) => m.generation === generation);
}

export function getMembersByBranch(branch: string): FamilyMemberFixture[] {
  return familyMemberFixtures.filter((m) => m.branch === branch || m.lineageBranchName === branch);
}

export function getMembersByGender(gender: 'Male' | 'Female'): FamilyMemberFixture[] {
  return familyMemberFixtures.filter((m) => m.gender === gender);
}

export function getLivingMembers(): FamilyMemberFixture[] {
  return familyMemberFixtures.filter((m) => m.status === 'Living');
}

export function getDeceasedMembers(): FamilyMemberFixture[] {
  return familyMemberFixtures.filter((m) => m.status === 'Deceased');
}

export function getChildrenOf(memberId: string): FamilyMemberFixture[] {
  return familyMemberFixtures.filter((m) => m.fatherId === memberId);
}

export function getAncestorsOf(memberId: string): FamilyMemberFixture[] {
  const ancestors: FamilyMemberFixture[] = [];
  let current = getMemberById(memberId);

  while (current?.fatherId) {
    const father = getMemberById(current.fatherId);
    if (father) {
      ancestors.push(father);
      current = father;
    } else {
      break;
    }
  }

  return ancestors;
}

export function getDescendantsOf(memberId: string): FamilyMemberFixture[] {
  const descendants: FamilyMemberFixture[] = [];
  const children = getChildrenOf(memberId);

  for (const child of children) {
    descendants.push(child);
    descendants.push(...getDescendantsOf(child.id));
  }

  return descendants;
}

export function getRootMember(): FamilyMemberFixture {
  return familyMemberFixtures.find((m) => m.fatherId === null)!;
}

export function getAllBranches(): string[] {
  const branches = new Set<string>();
  familyMemberFixtures.forEach((m) => {
    if (m.lineageBranchName) {
      branches.add(m.lineageBranchName);
    }
  });
  return Array.from(branches);
}

export function getGenerationStats(): Record<number, { total: number; male: number; female: number; living: number; deceased: number }> {
  const stats: Record<number, { total: number; male: number; female: number; living: number; deceased: number }> = {};

  for (let gen = 1; gen <= 6; gen++) {
    const members = getMembersByGeneration(gen);
    stats[gen] = {
      total: members.length,
      male: members.filter((m) => m.gender === 'Male').length,
      female: members.filter((m) => m.gender === 'Female').length,
      living: members.filter((m) => m.status === 'Living').length,
      deceased: members.filter((m) => m.status === 'Deceased').length,
    };
  }

  return stats;
}

